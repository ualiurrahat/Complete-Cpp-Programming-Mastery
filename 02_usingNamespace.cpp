/**
 * @file 02_usingNamespace.cpp
 * @brief Basic "Hello World" program in C++.
 *
 * This is the first program to demonstrate:
 * - How to include header files in C++ (`#include <iostream>`).
 * - Use of `main()` as the entry point.
 * - Printing output using `cout`.
 */

#include <iostream>
using namespace std; // Standard namespace

int main()
{
    cout << "Hello World!"; // Prints message to console
    return 0;               // Successful program termination
}
