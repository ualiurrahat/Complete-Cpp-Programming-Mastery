/**
 * @file 02_triangle90Degree.cpp
 * @brief Homework 2A: Print a right-angled triangle using '*'.
 *
 * @details
 * Demonstrates printing patterns using multiple cout statements.
 * Each line has increasing number of '*'.
 *
 * Expected output:
 * *
 * * *
 * * * *
 * * * * *
 * * * * * *
 */

#include <iostream>
using namespace std;

int main()
{
    cout << "*" << endl;
    cout << "* *" << endl;
    cout << "* * *" << endl;
    cout << "* * * *" << endl;
    cout << "* * * * *" << endl;

    return 0;
}
