/**
 * @file 01_basicVariableTypes.cpp
 * @brief Demonstrates basic variable types in C++: int and double.
 *
 * @details
 * - int: stores whole numbers.
 * - double: stores fractional numbers (floating point).
 * - cout: prints values to console.
 */

#include <iostream>
using namespace std;

int main()
{
    // int for integer values
    int age = 55;
    cout << age << "\n"; // prints 55

    // double for fractional numbers
    double weight = 92.5;
    cout << "My weight is " << weight << "\n"; // prints: My weight is 92.5

    return 0;
}
